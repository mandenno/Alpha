package com.swahilibox.mobile.vision.qrcode;


public class Contactfavs {
    String id;
    String image;
    String subject;
    String message;
    String campaign;
    String reg_date;

    public Contactfavs() {
    }

    public String getSubject() {
        return subject;
    }
    public String getId() {
        return id;
    }
    public String getMessage() {
        return message;
    }
    public String getCampaign() {
        return campaign;
    }

    public String getImage() {
        return image;
    }

    public String getReg_date() {
        return reg_date;
    }

}
