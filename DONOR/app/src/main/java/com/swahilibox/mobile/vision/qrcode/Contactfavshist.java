package com.swahilibox.mobile.vision.qrcode;


public class Contactfavshist {
    String id;
    String image;
    String campaign;
    String description;
    String location;
    String trackid;
    String reg_date;


    public Contactfavshist() {
    }

    public String getCampaign() {
        return campaign;
    }
    public String getTrackid() {
        return trackid;
    }
    public String getId() {
        return id;
    }
    public String getDescription() {
        return description;
    }
    public String getLocation() {
        return location;
    }
    public String getImage() {
        return image;
    }
    public String getDriveDate() {
        return reg_date;
    }


}
